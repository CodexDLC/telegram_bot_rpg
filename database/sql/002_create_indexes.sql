-- 002_create_indexes.sql
-- Этот скрипт создает индексы для ускорения выборок по часто
-- используемым полям, в основном по внешним ключам.

-- =================================================================
-- Индекс для таблицы: characters
-- Поле: user_id
-- Описание: Ускоряет поиск всех персонажей, принадлежащих одному
--           пользователю.
-- =================================================================
CREATE INDEX IF NOT EXISTS idx_characters_user_id ON characters (user_id);

-- =================================================================
-- Индекс для таблицы: character_stats
-- Поле: character_id
-- Описание: Хотя это поле является PRIMARY KEY и уже индексировано,
--           создание явного индекса не повредит и может быть
--           полезно для некоторых оптимизаторов запросов.
--           В данном случае, это скорее для полноты картины.
-- =================================================================
-- CREATE INDEX IF NOT EXISTS idx_character_stats_character_id ON character_stats (character_id);
-- Примечание: В SQLite PRIMARY KEY в большинстве случаев уже создает
-- уникальный индекс, поэтому дополнительный индекс не требуется.

-- В будущих версиях здесь могут быть добавлены индексы для других
-- таблиц, например, для `character_skills` по `character_id`.
